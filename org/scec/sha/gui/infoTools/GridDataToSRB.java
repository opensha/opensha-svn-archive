package org.scec.sha.gui.infoTools;

import java.io.*;
import org.scec.cme.SRBDrop.SRBDrop;


/**
 * <p>Title: GridDataToSRB</p>
 * <p>Description: A standalone that puts the Hazard Curves calculated using the
 *  grid to the SRB.</p>
 * @author unascribed
 * @version 1.0
 */

public class GridDataToSRB {


  /**
   * Accepts the 2 command line arguments:
   * localDir: path of the directory local directory we intent to put in the SRB
   * SRBDir: logical path of directory in the SRB where we want to put the above directory.
   * @param args:
   * args[0] : Path of the local directory
   * args[1] : Path of the remote SRB directory
   */
  public static void main(String[] args) {
    try{
      SRBDrop srb = new SRBDrop(true);
      srb.directoryPut(args[0],args[1],true);
    }catch(FileNotFoundException e){
      e.printStackTrace();
    }catch(IOException e){
      e.printStackTrace();
    }

  }
}